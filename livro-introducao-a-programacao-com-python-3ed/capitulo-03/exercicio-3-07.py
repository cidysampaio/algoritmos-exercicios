"""Capítulo 3 - Variáveis e entrada de dados

3.7) Faça um programa que peça dois números inteiros. Imprima a soma desses dois números na tela.
"""

num_um = int(input('Digite o primeiro número: '))
num_dois = int(input('Digite o segundo número: '))

soma = num_um + num_dois

print(f'\nA soma entre os números {num_um} e {num_dois} tem o resultado de {soma}')
